package day04_dataCasting_Increment;

public class C05_Increment {
    public static void main(String[] args) {


        int numA=2;
        int numB=6;
        numA+=3;
        System.out.println(numA);
        numA++;
        System.out.println(numA);
        numA=++numB;
        System.out.println(numB);
        System.out.println(numA);
        numB*=2;
        System.out.println(numB);
    }
}
